package models;

public class Booking {
	private String from_date;
	private String to_date;
	private String name;
	private String contact_number;
	private String type;
	private String brand;
	private String model;
	private String color;
	private String no_of_seats;
	private String insurance_coverage;
	private String rate;
	private String booked_date;
	
	public String getBooked_date() {
		return booked_date;
	}
	public void setBooked_date(String booked_date) {
		this.booked_date = booked_date;
	}
	public String getFrom_date() {
		return from_date;
	}
	public void setFrom_date(String from_date) {
		this.from_date = from_date;
	}
	public String getTo_date() {
		return to_date;
	}
	public void setTo_date(String to_date) {
		this.to_date = to_date;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContact_number() {
		return contact_number;
	}
	public void setContact_number(String contact_number) {
		this.contact_number = contact_number;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getNo_of_seats() {
		return no_of_seats;
	}
	public void setNo_of_seats(String no_of_seats) {
		this.no_of_seats = no_of_seats;
	}
	public String getInsurance_coverage() {
		return insurance_coverage;
	}
	public void setInsurance_coverage(String insurance_coverage) {
		this.insurance_coverage = insurance_coverage;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	
	
}
