package models;

public class Vehicle {
   private int id;
   private String type;
   private String brand;
   private String model;
   private String year;
   private String reg_no;
   private String color;
   private String no_of_seats;
   private String chassis_no;
   private String engine_no;
   private String mileage;
   private String insuarance_coverage;
   private String rate;
   private String image_path;
   
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}

   
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public String getBrand() {
	return brand;
}
public void setBrand(String brand) {
	this.brand = brand;
}
public String getModel() {
	return model;
}
public void setModel(String model) {
	this.model = model;
}
public String getYear() {
	return year;
}
public void setYear(String year) {
	this.year = year;
}
public String getReg_no() {
	return reg_no;
}
public void setReg_no(String reg_no) {
	this.reg_no = reg_no;
}
public String getColor() {
	return color;
}
public void setColor(String color) {
	this.color = color;
}
public String getNo_of_seats() {
	return no_of_seats;
}
public void setNo_of_seats(String no_of_seats) {
	this.no_of_seats = no_of_seats;
}
public String getChassis_no() {
	return chassis_no;
}
public void setChassis_no(String chassis_no) {
	this.chassis_no = chassis_no;
}
public String getEngine_no() {
	return engine_no;
}
public void setEngine_no(String engine_no) {
	this.engine_no = engine_no;
}
public String getMileage() {
	return mileage;
}
public void setMileage(String mileage) {
	this.mileage = mileage;
}
public String getInsuarance_coverage() {
	return insuarance_coverage;
}
public void setInsuarance_coverage(String insuarance_coverage) {
	this.insuarance_coverage = insuarance_coverage;
}
public String getRate() {
	return rate;
}
public void setRate(String rate) {
	this.rate = rate;
}
public String getImage_path() {
	return image_path;
}
public void setImage_path(String image_path) {
	this.image_path = image_path;
}

}
